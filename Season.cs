namespace BasterBoer.Core.Time
{
	/// <summary>
	/// South African seasons (Southern Hemisphere).
	/// </summary>
	public enum Season
	{
		/// <summary>Summer: December, January, February - Hot, wet season with peak grass growth.</summary>
		Summer = 0,
		
		/// <summary>Autumn: March, April, May - Cooling temperatures, declining rainfall.</summary>
		Autumn = 1,
		
		/// <summary>Winter: June, July, August - Cold, dry season with minimal grass growth.</summary>
		Winter = 2,
		
		/// <summary>Spring: September, October, November - Warming temperatures, first rains expected.</summary>
		Spring = 3
	}
}
